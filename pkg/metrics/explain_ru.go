package metrics

import (
	metricscommon "github.com/pingcap/tidb/pkg/metrics/common"
	"github.com/prometheus/client_golang/prometheus"
)

const (
	explainRULabelStatus                  = "status"
	explainRULabelComponentSnapshotStatus = "component_snapshot_status"
	readBillingDemoLabelModelVersion      = "model_version"
)

// EXPLAIN FORMAT='RU' demo metrics.
var (
	ExplainRUPreviewRUCounter         *prometheus.CounterVec
	ExplainRUWorkRowsCounter          *prometheus.CounterVec
	ExplainRUWorkBytesCounter         *prometheus.CounterVec
	ExplainRURowWidthHistogram        *prometheus.HistogramVec
	ExplainRUStatementsCounter        *prometheus.CounterVec
	ExplainRURenderDurationHistogram  *prometheus.HistogramVec
	ExplainRUComponentSnapshotCounter *prometheus.CounterVec

	ReadBillingDemoStatementsCounter     *prometheus.CounterVec
	ReadBillingDemoOperatorStatusCounter *prometheus.CounterVec
	ReadBillingDemoBaseUnitsCounter      *prometheus.CounterVec
	ReadBillingDemoRowWidthHistogram     *prometheus.HistogramVec
)

// InitExplainRUMetrics initializes metrics for EXPLAIN ANALYZE FORMAT='RU'.
func InitExplainRUMetrics() {
	// Keep these labels bounded during demo calibration. SQL text, digest,
	// plan ID, table name, and index name belong in the SQL result, not metrics.
	ExplainRUPreviewRUCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "preview_ru_total",
			Help:      "Counter of read billing preview RU values generated by EXPLAIN ANALYZE FORMAT='RU'.",
		}, []string{"section", "component", "operator", "source"},
	)
	ExplainRUWorkRowsCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "work_rows_total",
			Help:      "Counter of row-count model inputs generated by EXPLAIN ANALYZE FORMAT='RU'.",
		}, []string{"section", "component", "operator", "source"},
	)
	ExplainRUWorkBytesCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "work_bytes_total",
			Help:      "Counter of byte-shaped model inputs generated by EXPLAIN ANALYZE FORMAT='RU'.",
		}, []string{"section", "component", "operator", "source"},
	)
	ExplainRUStatementsCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "statements_total",
			Help:      "Counter of EXPLAIN ANALYZE FORMAT='RU' statement status.",
		}, []string{explainRULabelStatus},
	)
	ExplainRURenderDurationHistogram = metricscommon.NewHistogramVec(
		prometheus.HistogramOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "render_duration_seconds",
			Help:      "Histogram of EXPLAIN ANALYZE FORMAT='RU' render duration.",
			Buckets:   prometheus.DefBuckets,
		}, []string{explainRULabelStatus},
	)
	ExplainRURowWidthHistogram = metricscommon.NewHistogramVec(
		prometheus.HistogramOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "row_width_bytes",
			Help:      "Histogram of row-width factors generated by EXPLAIN ANALYZE FORMAT='RU'.",
			Buckets:   prometheus.ExponentialBuckets(1, 2, 12),
		}, []string{"component", "operator", "source"},
	)
	ExplainRUComponentSnapshotCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "explain_ru",
			Name:      "component_snapshot_total",
			Help:      "Counter of EXPLAIN ANALYZE FORMAT='RU' component snapshot status.",
		}, []string{explainRULabelComponentSnapshotStatus},
	)
	ReadBillingDemoStatementsCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "read_billing_demo",
			Name:      "statements_total",
			Help:      "Counter of read billing demo statement status.",
		}, []string{"status", readBillingDemoLabelModelVersion},
	)
	ReadBillingDemoOperatorStatusCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "read_billing_demo",
			Name:      "operator_status_total",
			Help:      "Counter of read billing demo operator status.",
		}, []string{"site", "op_class", "operator_kind", "status", "reason", readBillingDemoLabelModelVersion},
	)
	ReadBillingDemoBaseUnitsCounter = metricscommon.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: "tidb",
			Subsystem: "read_billing_demo",
			Name:      "base_units_total",
			Help:      "Counter of coefficient-free read billing demo base units.",
		}, []string{"site", "op_class", "operator_kind", "unit", "input_source", "input_side", readBillingDemoLabelModelVersion},
	)
	ReadBillingDemoRowWidthHistogram = metricscommon.NewHistogramVec(
		prometheus.HistogramOpts{
			Namespace: "tidb",
			Subsystem: "read_billing_demo",
			Name:      "row_width_bytes",
			Help:      "Histogram of row-width factors used by read billing demo.",
			Buckets:   prometheus.ExponentialBuckets(1, 2, 12),
		}, []string{"site", "op_class", "operator_kind", "row_width_source", readBillingDemoLabelModelVersion},
	)
}

// RecordExplainRUStatus records a bounded FORMAT='RU' statement status.
func RecordExplainRUStatus(status string) {
	if ExplainRUStatementsCounter == nil || status == "" {
		return
	}
	ExplainRUStatementsCounter.WithLabelValues(status).Inc()
}

// ObserveExplainRURenderDuration records a bounded FORMAT='RU' render duration.
func ObserveExplainRURenderDuration(status string, seconds float64) {
	if ExplainRURenderDurationHistogram == nil || status == "" || seconds < 0 {
		return
	}
	ExplainRURenderDurationHistogram.WithLabelValues(status).Observe(seconds)
}

// RecordExplainRUComponentSnapshot records component snapshot availability.
func RecordExplainRUComponentSnapshot(status string) {
	if ExplainRUComponentSnapshotCounter == nil || status == "" {
		return
	}
	ExplainRUComponentSnapshotCounter.WithLabelValues(status).Inc()
}

// ObserveExplainRURow records bounded numeric samples from generated FORMAT='RU' rows.
func ObserveExplainRURow(section, component, operator, source, rowWidthSource string, previewRU, workRows, workBytes, rowWidth float64) {
	// Negative values are sentinels for "not present" so callers can use one
	// path for summary and plan rows without emitting fake zeros.
	if ExplainRUPreviewRUCounter != nil && previewRU >= 0 {
		ExplainRUPreviewRUCounter.WithLabelValues(section, component, operator, source).Add(previewRU)
	}
	if ExplainRUWorkRowsCounter != nil && workRows >= 0 {
		ExplainRUWorkRowsCounter.WithLabelValues(section, component, operator, source).Add(workRows)
	}
	if ExplainRUWorkBytesCounter != nil && workBytes >= 0 {
		ExplainRUWorkBytesCounter.WithLabelValues(section, component, operator, source).Add(workBytes)
	}
	if ExplainRURowWidthHistogram != nil && section == "plan" && rowWidth > 0 && operator != "" {
		ExplainRURowWidthHistogram.WithLabelValues(component, operator, rowWidthSource).Observe(rowWidth)
	}
}

// RecordReadBillingDemoStatement records a bounded read billing demo statement status.
func RecordReadBillingDemoStatement(status, modelVersion string) {
	if ReadBillingDemoStatementsCounter == nil || status == "" || modelVersion == "" {
		return
	}
	ReadBillingDemoStatementsCounter.WithLabelValues(status, modelVersion).Inc()
}

// RecordReadBillingDemoOperatorStatus records a bounded read billing demo operator status.
func RecordReadBillingDemoOperatorStatus(site, opClass, operatorKind, status, reason, modelVersion string) {
	if ReadBillingDemoOperatorStatusCounter == nil ||
		site == "" || opClass == "" || operatorKind == "" || status == "" || reason == "" || modelVersion == "" {
		return
	}
	ReadBillingDemoOperatorStatusCounter.WithLabelValues(site, opClass, operatorKind, status, reason, modelVersion).Inc()
}

// AddReadBillingDemoBaseUnits records coefficient-free read billing demo base units.
func AddReadBillingDemoBaseUnits(site, opClass, operatorKind, unit, inputSource, inputSide, modelVersion string, value float64) {
	if ReadBillingDemoBaseUnitsCounter == nil ||
		site == "" || opClass == "" || operatorKind == "" || unit == "" || inputSource == "" || inputSide == "" || modelVersion == "" ||
		value <= 0 {
		return
	}
	ReadBillingDemoBaseUnitsCounter.WithLabelValues(site, opClass, operatorKind, unit, inputSource, inputSide, modelVersion).Add(value)
}

// ObserveReadBillingDemoRowWidth records row-width factors used by read billing demo.
func ObserveReadBillingDemoRowWidth(site, opClass, operatorKind, rowWidthSource, modelVersion string, rowWidth float64) {
	if ReadBillingDemoRowWidthHistogram == nil ||
		site == "" || opClass == "" || operatorKind == "" || rowWidthSource == "" || modelVersion == "" || rowWidth <= 0 {
		return
	}
	ReadBillingDemoRowWidthHistogram.WithLabelValues(site, opClass, operatorKind, rowWidthSource, modelVersion).Observe(rowWidth)
}
